#include <stdio.h>


/*

    Caracteres de escape
    Quebra de linha -> \n 
    Tabulação ->       \t 
    Aspas dupla ->     \" 
    Aspas simples ->   \' 
    Contrabarra ->     \\

*/

int main(){


    // Caracteres de Escape

// \n 
printf("Oi,\nMe chamo Guilherme, conhecido por ser muito bom no Blade Ball\n\n\n\n"); 

// \t 
printf("Oi,\tMe chamo Guilherme, conhecido por ser muito bom no Blade Ball\n\n\n");

// \"
printf("Oi,\"Me chamo Guilherme\", conhecido por ser muito bom no Blade Ball\n\n\n");

// \'
printf("Oi,\'Me chamo Guilherme\', conhecido por ser muito bom no Blade Ball\n\n\n");

// Contrabarra (\\)
printf("Oi,\\Me chamo Guilherme\\, conhecido por ser muito bom no Blade Ball\n\n\n");



    return 0;
}
