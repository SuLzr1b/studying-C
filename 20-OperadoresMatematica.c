#include <stdio.h>

/*

    Operadores matemáticos para:
    Soma          +
    Subtração     -
    Multiplicação *
    Divisão       /

*/

int main(){

    printf("\nSoma: %d\n", 10 + 20);

    printf("\nSubtracao: %d\n", 10 - 20);

    printf("\nMultiplicacao: %d\n", 10 * 20);

    printf("\nDivisao: %f\n\n", 10.0 / 20);







    return 0;
}

